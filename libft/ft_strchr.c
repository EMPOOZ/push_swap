#include"libft.h"

char	*ft_strchr(const char *p, int i)
{
	int	y;

	y = 0;
	while (p[y] != (unsigned char)i)
	{
		if (!p[y])
			return (0);
		y++;
	}
	return ((char *)&p[y]);
}
